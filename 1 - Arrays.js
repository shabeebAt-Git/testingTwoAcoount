const arr = [1,2,3,"shabeeb"]


for(const item of arr){
    console.log(item);
}


arr.unshift(5)   // To add at start
console.log(arr);

arr.shift()      //  To remove from start
console.log(arr);

arr.push(4)     // To add at end
console.log(arr);

arr.pop()        //  to Remove from end
console.log(arr);









